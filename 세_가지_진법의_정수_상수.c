//변수: 값이 바뀌는 데이터(나이, 키, 몸무게)
//상수: 값이 바뀌지 않는 데이터(태어난 시간, 태어난 장소, 생물학적 부모)

#include<stdio.h>

int main(void)
{
    printf("%o\n", 12);     // 8진수로 출력합니다
    printf("%d\n", 12);     // 10진수로 출력합니다
    printf("%x\n", 12);     // 16진수롤 소문자로 출력
    printf("%X\n", 12);     // 16진수를 대문자로 출력

    return 0;
}